// ─── Framework Detection ──────────────────────────────────────────────────────
function detectFramework(cwd) {
  const pkgPath = path.join(cwd, 'package.json');
  if (!fs.existsSync(pkgPath)) return 'node';

  try {
    const pkg = JSON.parse(fs.readFileSync(pkgPath, 'utf8'));
    const deps = {
      ...((pkg.dependencies) || {}),
      ...((pkg.devDependencies) || {})
    };

    if (deps['next'])        return 'Next.js';
    if (deps['@nestjs/core']) return 'NestJS';
    if (deps['fastify'])     return 'Fastify';
    if (deps['express'])     return 'Express';
    if (deps['koa'])         return 'Koa';
    if (deps['hapi'])        return 'Hapi';
    return 'Node.js';
  } catch (_) {
    return 'Node.js';
  }
}

// ─── .env Helpers ─────────────────────────────────────────────────────────────
function readEnvFile(envPath) {
  if (!fs.existsSync(envPath)) return {};
  const lines = fs.readFileSync(envPath, 'utf8').split('\n');
  const vars = {};
  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const eqIdx = trimmed.indexOf('=');
    if (eqIdx < 0) continue;
    const key = trimmed.slice(0, eqIdx).trim();
    const val = trimmed.slice(eqIdx + 1).trim();
    vars[key] = val;
  }
  return vars;
}

function appendEnvVars(envPath, vars) {
  const existing = readEnvFile(envPath);
  const toAppend = [];
  const toCreate = !fs.existsSync(envPath);

  if (toCreate) {
    toAppend.push('# DDS Authentication Platform');
    toAppend.push('# Generated by: npx dds init');
    toAppend.push('# Documentation: https://dds.example.com/docs');
    toAppend.push('');
  } else {
    // Add a separator comment if the file already has content
    const content = fs.readFileSync(envPath, 'utf8');
    if (content.trim() && !content.includes('DDS_APP_ID')) {
      toAppend.push('');
      toAppend.push('# ─── DDS Authentication Platform ─────────────────────');
      toAppend.push('# Generated by: npx dds init');
    }
  }

  const added = [];
  for (const [key, value] of Object.entries(vars)) {
    if (existing[key]) {
      // Key exists — skip, never overwrite
      continue;
    }
    toAppend.push(`${key}=${value}`);
    added.push(key);
  }

  if (toAppend.length > 0) {
    const suffix = toCreate ? '' : '\n';
    fs.appendFileSync(envPath, suffix + toAppend.join('\n') + '\n');
  }

  return { existing, added };
}

// ─── HTTP Ping ────────────────────────────────────────────────────────────────
function httpGet(url, timeoutMs = 5000) {
  return new Promise((resolve) => {
    const lib = url.startsWith('https') ? https : http;
    const req = lib.get(url, { timeout: timeoutMs }, (res) => {
      let data = '';
      res.on('data', (chunk) => { data += chunk; });
      res.on('end', () => {
        try { resolve({ ok: true, status: res.statusCode, body: JSON.parse(data) }); }
        catch (_) { resolve({ ok: true, status: res.statusCode, body: {} }); }
      });
    });
    req.on('error', (err) => resolve({ ok: false, error: err.message }));
    req.on('timeout', () => { req.destroy(); resolve({ ok: false, error: 'Connection timed out' }); });
  });
}

// ─── Readline Prompt ──────────────────────────────────────────────────────────
function createRl() {
  return readline.createInterface({
    input:  process.stdin,
    output: process.stdout
  });
}

function prompt(rl, question) {
  return new Promise((resolve) => {
    rl.question(question, (answer) => resolve(answer.trim()));
  });
}

// ─── Validation ───────────────────────────────────────────────────────────────
const APP_ID_RE     = /^app_[A-Za-z0-9_]{16,64}$/;
const PUBLIC_KEY_RE = /^dds_pk_[A-Za-z0-9_]{16,128}$/;
const SECRET_KEY_RE = /^dds_sk_[A-Za-z0-9_]{16,128}$/;

function validateFormat(key, value) {
  switch (key) {
    case 'DDS_APP_ID':     return APP_ID_RE.test(value);
    case 'DDS_PUBLIC_KEY': return PUBLIC_KEY_RE.test(value);
    case 'DDS_SECRET_KEY': return SECRET_KEY_RE.test(value);
    default: return true;
  }
}

function formatHint(key) {
  switch (key) {
    case 'DDS_APP_ID':     return 'app_xxxxxxxxxxxxxxxxxxxxxxxx';
    case 'DDS_PUBLIC_KEY': return 'dds_pk_xxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxx';
    case 'DDS_SECRET_KEY': return 'dds_sk_xxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxx';
    default: return '';
  }
}

// ─── Print Results Table ──────────────────────────────────────────────────────
function printSummary({ framework, envPath, added, existing, connectivity, baseUrl }) {
  console.log('');
  console.log('  ' + dim('─'.repeat(50)));
  console.log('');
  console.log(bold('  ✅ DDS Setup Complete!'));
  console.log('');

  const rows = [
    ['Framework',      cyan(framework)],
    ['Config file',    dim(envPath)],
  ];

  if (added.length > 0) {
    rows.push(['Variables added', green(added.join(', '))]);
  }
  if (Object.keys(existing).filter(k => k.startsWith('DDS_') && !added.includes(k)).length > 0) {
    const kept = Object.keys(existing).filter(k => k.startsWith('DDS_') && !added.includes(k));
    rows.push(['Already set', dim(kept.join(', ') + ' (unchanged)')]);
  }

  rows.push(['DDS Server', connectivity.ok ? green('Connected ✓') : red('Unreachable ✗')]);
  rows.push(['Server URL',   dim(baseUrl)]);

  for (const [label, value] of rows) {
    const pad = ' '.repeat(Math.max(0, 18 - label.length));
    console.log(`  ${bold(label)}${pad}${value}`);
  }

  console.log('');
  console.log('  ' + dim('─'.repeat(50)));
  console.log('');
  console.log(bold('  🚀 Next Steps'));
  console.log('');
  console.log(dim('  1.') + ' Make sure your DDS Server is running');
  console.log(dim('  2.') + ' Use the SDK in your application:');
  console.log('');
  console.log(dim('     ') + cyan('const') + ' { DDS } = require(' + green('"@dds/server"') + ');');
  console.log(dim('     ') + cyan('const') + ' dds = ' + cyan('new') + ' DDS();');
  console.log('');
  console.log(dim('     ') + cyan('const') + ' result = ' + cyan('await') + ' dds.requestAuth({');
  console.log(dim('     ') + '  mobileNumber: ' + green('"+919876543210"'));
  console.log(dim('     ') + '});');
  console.log('');
  console.log('  ' + dim('─'.repeat(50)));
  console.log('');
  console.log(dim('  📚 Documentation: https://dds.example.com/docs'));
  console.log(dim('  🐛 Support: https://dds.example.com/support'));
  console.log('');
}

// ─── Main ─────────────────────────────────────────────────────────────────────
async function main() {
  const args = process.argv.slice(2);

  // Handle --help / -h
  if (args.includes('--help') || args.includes('-h')) {
    printBanner();
    console.log('  ' + bold('Usage:') + '  npx dds init [options]');
    console.log('');
    console.log('  ' + bold('Commands:'));
    console.log('    init          Initialize DDS in the current project (default)');
    console.log('');
    console.log('  ' + bold('Options:'));
    console.log('    --help, -h    Show this help message');
    console.log('    --version, -v Show the @dds/server version');
    console.log('    --yes, -y     Skip prompts and use environment variables as-is');
    console.log('');
    console.log('  ' + bold('Environment Variables:'));
    console.log('    DDS_APP_ID       Your DDS Application ID');
    console.log('    DDS_PUBLIC_KEY   Your DDS Public API Key');
    console.log('    DDS_SECRET_KEY   Your DDS Secret API Key');
    console.log('    DDS_BASE_URL     DDS Server URL (default: http://localhost:5000)');
    console.log('');
    return;
  }

  // Handle --version / -v
  if (args.includes('--version') || args.includes('-v')) {
    try {
      const pkg = JSON.parse(fs.readFileSync(path.join(__dirname, '..', 'package.json'), 'utf8'));
      console.log(`@dds/server v${pkg.version}`);
    } catch (_) {
      console.log('@dds/server v1.0.0');
    }
    return;
  }

  const skipPrompts = args.includes('--yes') || args.includes('-y');
  const cwd = process.cwd();
  const envPath = path.join(cwd, '.env');

  printBanner();

  // ── Step 1: Detect framework ──────────────────────────────────────────────
  const framework = detectFramework(cwd);
  console.log(`  ${bold('Framework detected:')} ${cyan(framework)}`);
  console.log('');

  // ── Step 2: Read existing .env ────────────────────────────────────────────
  const existingEnv = readEnvFile(envPath);
  const envExists   = fs.existsSync(envPath);

  if (envExists) {
    console.log(`  ${green('✓')} Found existing ${dim('.env')} file`);
  } else {
    console.log(`  ${yellow('+')} No .env found — will create one`);
  }
  console.log('');

  // Determine which DDS vars are missing
  const DDS_VARS = ['DDS_APP_ID', 'DDS_PUBLIC_KEY', 'DDS_SECRET_KEY'];
  const missing = DDS_VARS.filter(k => !existingEnv[k]);
  const baseUrl = existingEnv['DDS_BASE_URL'] || process.env.DDS_BASE_URL || 'http://localhost:5000';

  let credentialsToWrite = {};

  if (missing.length === 0 && !skipPrompts) {
    console.log(`  ${green('✓')} All DDS credentials are already configured in .env`);
    console.log('');
  } else if (!skipPrompts) {
    // ── Step 3: Prompt for credentials ───────────────────────────────────────
    console.log(`  ${bold('Configure DDS Credentials')}`);
    console.log(dim('  Copy your credentials from the DDS Developer Portal'));
    console.log(dim('  → Applications → Select App → Credentials'));
    console.log('');

    const rl = createRl();

    for (const key of missing) {
      const hint = formatHint(key);
      let value = '';
      let valid = false;

      while (!valid) {
        value = await prompt(rl, `  ${bold(key)} ${dim('(' + hint + ')')}\n  ${cyan('>')} `);
        if (!value) {
          console.log(`  ${yellow('⚠')} ${key} cannot be empty. Try again.\n`);
          continue;
        }
        if (!validateFormat(key, value)) {
          if (key === 'DDS_APP_ID') {
            console.log(`  ${red('✗')} Invalid format. Must start with ${bold('app_')} followed by 16–64 URL-safe characters.\n`);
          } else if (key === 'DDS_PUBLIC_KEY') {
            console.log(`  ${red('✗')} Invalid format. Must start with ${bold('dds_pk_')} followed by 16–128 URL-safe characters.\n`);
          } else if (key === 'DDS_SECRET_KEY') {
            console.log(`  ${red('✗')} Invalid format. Must start with ${bold('dds_sk_')} followed by 16–128 URL-safe characters.\n`);
          }
          continue;
        }
        valid = true;
        credentialsToWrite[key] = value;
        console.log(`  ${green('✓')} ${key} accepted\n`);
      }
    }

    // Optional: DDS_BASE_URL
    if (!existingEnv['DDS_BASE_URL']) {
      const customUrl = await prompt(rl,
        `  ${bold('DDS_BASE_URL')} ${dim('(Press Enter for default: http://localhost:5000)')}\n  ${cyan('>')} `
      );
      if (customUrl) {
        credentialsToWrite['DDS_BASE_URL'] = customUrl;
      }
    }

    rl.close();
  }

  // ── Step 4: Write .env ────────────────────────────────────────────────────
  const { added } = appendEnvVars(envPath, credentialsToWrite);
  if (added.length > 0) {
    console.log(`  ${green('✓')} Written to ${dim('.env')}: ${green(added.join(', '))}`);
    console.log('');
  }

  // ── Step 5: Test connectivity ─────────────────────────────────────────────
  console.log(`  ${bold('Testing DDS Server connectivity...')}`);
  const testUrl = (credentialsToWrite['DDS_BASE_URL'] || baseUrl).replace(/\/$/, '');
  const pingResult = await httpGet(`${testUrl}/api/config/public`, 6000);

  if (pingResult.ok) {
    const serverName = pingResult.body?.appName || 'DDS Server';
    console.log(`  ${green('✓')} Connected to ${bold(serverName)} at ${dim(testUrl)}`);
  } else {
    console.log(`  ${yellow('⚠')} Could not reach DDS Server at ${dim(testUrl)}`);
    console.log(`  ${dim('  ' + (pingResult.error || 'Unknown error'))}`);
    console.log(`  ${dim('  Make sure your DDS Server is running before using the SDK.')}`);
  }

  console.log('');

  // ── Step 6: Print summary ─────────────────────────────────────────────────
  printSummary({
    framework,
    envPath: path.relative(cwd, envPath),
    added,
    existing: existingEnv,
    connectivity: pingResult,
    baseUrl: testUrl
  });
}

// ─── Entry Point ─────────────────────────────────────────────────────────────
main().catch((err) => {
  console.error('');
  console.error(red('  ✗ DDS Init failed: ') + err.message);
  console.error('');
  process.exit(1);
});
